<div class="col-text-align:justify">Wardell Stephen Curry (lahir 14 Maret 1988) adalah Pemain Bola Basket Profesional Amerika dari Akron, Ohio. Dia saat ini mewakili Golden State Warriors di NBA. Selanjutnya, pemain berusia 30 tahun ini dipilih oleh Golden State Warriors pada 2009.

Ia memilih sebagai keseluruhan ketujuh dalam draft NBA 2009. Selain itu, ia menunjukkan keterampilan menembaknya sejak awal. Curry juga mencetak rekor 166 tiga poin sebagai rookie di liga. Bahkan, pemain itu memenangkan " NBA Most Valuable Player Award " dua kali berturut-turut. 
Pada bulan Februari 2014, Steph membuat penampilan All-Star pertamanya untuk Barat. Stephen kemudian dipilih untuk Tim All-NBA pertamanya. Pemain ini juga menjadi pencetak angka tiga angka terkemuka untuk Warriors. Dia memecahkan rekor liga sendiri dengan meraih 286 tembakan tiga angka.

Baru-baru ini, Curry mencetak 51 poin dan berada di peringkat kelima dalam daftar 3-poin NBA sepanjang masa. Selain itu, ia merobohkan 11 treys dalam kemenangan 144-122 atas Washington Wizards pada 24 Oktober 2018.
Pemain NBA Bintang ini lahir pada 14 Maret 1988 di Akron, Ohio, Amerika Serikat. Oleh karena itu, usia Stephen Curry adalah Tiga Puluh (30) tahun, sejak 2018. Ayahnya "Dell Curry" adalah mantan pemain NBA dan ibunya "Sonya" bermain bola voli di Virginia Tech.

Selanjutnya, saudaranya yang bernama Seth juga merupakan pemain bola basket profesional yang mewakili Dallas Mavericks. Adik Stephen bernama " Sydel Curry " bermain bola voli di Elon University. Pemain menghadiri Charlotte Christian High School untuk pendidikannya.

Selain itu, Curry kemudian mendaftar di Davidson College pada November 2005. Namun, ia meninggalkan College tanpa menyelesaikan tahun seniornya untuk bermain NBA.

Pada 30 Juli 2011, Steph Curry menikahi kekasihnya di perguruan tinggi "Ayesha Alexander" di Charlotte. Pasangan itu pertama kali bertemu dalam kelompok pemuda gereja ketika mereka masih remaja. Pasangan ini juga berbagi dua putri, Ryan & Riley dan seorang putra Canon.</div>
